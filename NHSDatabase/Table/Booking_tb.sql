CREATE TABLE Bookings_tb (
  BOOKING_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY constraint PK_BOOKINGID primary key,
  BOOKING_DATE DATE NOT NULL,
  BOOKING_TIME TIMESTAMP NOT NULL,
  DURATION NUMBER(4,2) NOT NULL,
  GUESTS NUMBER(2) NOT NULL,
  COLOUR VARCHAR2(20) constraint R_COLOUR CHECK (COLOUR in ('RED', 'BLUE', 'GREEN', 'YELLOW', 'PURPLE', 'ORANGE')),
  USER_ID NUMBER(11) NOT NULL constraint FK_USER_ID REFERENCES Users_tb,
  ROOM_ID NUMBER(11) NOT NULL constraint FK_ROOMS_ID REFERENCES Room_tb, 
  REVIEW_ID NUMBER(11) constraint FK_REVIEWS_ID REFERENCES Reviews_tb
);